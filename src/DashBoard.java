    private void sortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sortActionPerformed
        String selectedSortOption = (String) sort.getSelectedItem();
        if (selectedSortOption.equals("sort by id asc")) {
            fetchUsersSortAsc();
        } else if (selectedSortOption.equals("sort by id desc")) {
            fetchUsersSortDesc();
        } else if (selectedSortOption.equals("sort by total items asc")){
            fetchTotalSortAsc();
        }else if (selectedSortOption.equals("sort by total items desc")){
            fetchTotalSortDesc();
        }
    }//GEN-LAST:event_sortActionPerformed

