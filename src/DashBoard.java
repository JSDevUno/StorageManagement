    public void fetchUsersSortedAsc() {
        String selectedColumn = sort.getSelectedItem().toString(); 
        if (!isValidColumn(selectedColumn)) {
            JOptionPane.showMessageDialog(null, "Invalid column selected for sorting.");
            return;
        }

        try {
            String sql = "SELECT * FROM users ORDER BY " + selectedColumn + " ASC";
            pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            DefaultTableModel usersTableModel = (DefaultTableModel) viewuserstable.getModel();
            usersTableModel.setRowCount(0);

            while (rs.next()) {
                Object[] rowData = {
                    rs.getInt("User_ID"),
                    rs.getString("User_Name"),
                    rs.getString("Password"),
                    rs.getString("Email"),
                    rs.getString("Question"),
                    rs.getString("Answer"),
                    rs.getInt("total_items")
                };
                usersTableModel.addRow(rowData);
            }

            rs.close();
            pst.close();
        } catch (SQLException ex) {
            Logger.getLogger(DashBoard.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private boolean isValidColumn(String column) {
        String[] validColumns = {"User_ID", "User_Name", "Password", "Email", "Question", "Answer", "total_items"};
        for (String validColumn : validColumns) {
            if (validColumn.equals(column)) {
                return true;
            }
        }
        return false;
    }

