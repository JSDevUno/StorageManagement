    private void addItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addItemActionPerformed
        String itemName = name.getText();
        String itemDesc = (String) desc.getSelectedItem();

        try {
            int itemQuan = Integer.parseInt(quan.getText());

            if (itemName.isEmpty() || itemDesc.isEmpty()){
                JOptionPane.showMessageDialog(this, "All fields must be filled");
                return;
            }

            try {
                // Retrieve the current total_items value
                PreparedStatement selectStmt = conn.prepareStatement("SELECT total_items FROM users WHERE User_ID = ?");
                selectStmt.setString(1, String.valueOf(userID));
                ResultSet rs = selectStmt.executeQuery();
                int currentTotalItems = 0;
                if (rs.next()) {
                    currentTotalItems = rs.getInt("total_items");
                }

                // Calculate the new total_items value
                int newTotalItems = currentTotalItems + itemQuan;

                // Update the total_items field in the users table
                PreparedStatement updateStmt = conn.prepareStatement("UPDATE users SET total_items = ? WHERE User_ID = ?");
                updateStmt.setInt(1, newTotalItems);
                updateStmt.setString(2, String.valueOf(userID));
                updateStmt.executeUpdate();

                // Insert the new item into the item table
                pst = conn.prepareStatement("INSERT INTO item (ID, Item_Name, Item_Quantity, Item_Description) VALUES(?,?,?,?)");
                pst.setString(1, String.valueOf(userID));
                pst.setString(2, itemName);
                pst.setString(3, String.valueOf(itemQuan));
                pst.setString(4, itemDesc);

                int k = pst.executeUpdate();

                if(k == 1) {
                    name.setText("");
                    quan.setText("");
                    desc.setSelectedIndex(0); 

                    JOptionPane.showMessageDialog(this, "SUCCESFULLY ADDED");  
                } 
            } catch (SQLException ex) {
                Logger.getLogger(SignUp.class.getName()).log(Level.SEVERE, null, ex);
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Please enter a valid integer.");
            quan.setText(""); 
        }
    }//GEN-LAST:event_addItemActionPerformed

