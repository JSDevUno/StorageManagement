    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        String searched = jTextField10.getText();
        String sql = "DELETE FROM item WHERE Item_Name = ?";

        try {
            // Retrieve the quantity of the item being deleted
            PreparedStatement selectStmt = conn.prepareStatement("SELECT Item_Quantity FROM item WHERE Item_Name = ?");
            selectStmt.setString(1, searched);
            ResultSet rs = selectStmt.executeQuery();
            int itemQuan = 0;
            if (rs.next()) {
                itemQuan = rs.getInt("Item_Quantity");
            }

            pst = conn.prepareStatement(sql);
            pst.setString(1, searched);
            System.out.println("Executing delete..."); // Debug print
            int rowsAffected = pst.executeUpdate();

            if (rowsAffected > 0) {
                // Update total_items in the users table
                PreparedStatement updateStmt = conn.prepareStatement("UPDATE users SET total_items = total_items - ? WHERE User_ID = ?");
                updateStmt.setInt(1, itemQuan);
                updateStmt.setString(2, String.valueOf(userID));
                updateStmt.executeUpdate();

                JOptionPane.showMessageDialog(null, "Item deleted successfully");
                jTextField10.setText("");
                removeID.setText("");
                removeName.setText("");
                removeQuan.setText("");
                removeDesc.setText(""); 
            } else {
                JOptionPane.showMessageDialog(null, "Item not found");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error deleting item: " + e.getMessage());
            e.printStackTrace(); // Debug print to console
        } finally {
            try {
                if (pst != null) pst.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error closing statement: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton8ActionPerformed

